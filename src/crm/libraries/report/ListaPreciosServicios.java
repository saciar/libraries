package crm.libraries.report;

public class ListaPreciosServicios {

	private String servicio;
	private double unDia;
	private long codigoServicio;
	private double dosDias;
	private double tresDias;
	private double cuatroDias;
	private double cincoDias;
	/**
	 * @return Returns the cincoDias.
	 */
	public double getCincoDias() {
		return cincoDias;
	}
	/**
	 * @param cincoDias The cincoDias to set.
	 */
	public void setCincoDias(double cincoDias) {
		this.cincoDias = cincoDias;
	}
	/**
	 * @return Returns the codigoServicio.
	 */
	public long getCodigoServicio() {
		return codigoServicio;
	}
	/**
	 * @param codigoServicio The codigoServicio to set.
	 */
	public void setCodigoServicio(long codigoServicio) {
		this.codigoServicio = codigoServicio;
	}
	/**
	 * @return Returns the cuatroDias.
	 */
	public double getCuatroDias() {
		return cuatroDias;
	}
	/**
	 * @param cuatroDias The cuatroDias to set.
	 */
	public void setCuatroDias(double cuatroDias) {
		this.cuatroDias = cuatroDias;
	}
	/**
	 * @return Returns the dosDias.
	 */
	public double getDosDias() {
		return dosDias;
	}
	/**
	 * @param dosDias The dosDias to set.
	 */
	public void setDosDias(double dosDias) {
		this.dosDias = dosDias;
	}
	/**
	 * @return Returns the servicio.
	 */
	public String getServicio() {
		return servicio;
	}
	/**
	 * @param servicio The servicio to set.
	 */
	public void setServicio(String servicio) {
		this.servicio = servicio;
	}
	/**
	 * @return Returns the tresDias.
	 */
	public double getTresDias() {
		return tresDias;
	}
	/**
	 * @param tresDias The tresDias to set.
	 */
	public void setTresDias(double tresDias) {
		this.tresDias = tresDias;
	}
	/**
	 * @return Returns the unDia.
	 */
	public double getUnDia() {
		return unDia;
	}
	/**
	 * @param unDia The unDia to set.
	 */
	public void setUnDia(double unDia) {
		this.unDia = unDia;
	}

}
